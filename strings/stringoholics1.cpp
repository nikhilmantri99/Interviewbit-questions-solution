long long int M=pow(10,9)+7;

long long int min_time_to_get_same_string(string &x){
	long long int len=x.size();
	string y=x;
	y.append(x);
	for(long long int t=1;;t++){
		long long int i=((t%len)*(t%len+1)/2)%len;
		if(y.substr(i,len)==x){
			return t%M;
		}
	}
}

long long int gcd(long long int a, long long int b){
	if(a==0){
		return b;
	}
	else if(b==0){
		return a;
	}
	return gcd(b%a,a);
}

int solve(vector<string> &A) {
	long long int ans;
	long long int time=min_time_to_get_same_string(A[0])%M;
	for(int i=1;i<A.size();i++){
		long long int time1=min_time_to_get_same_string(A[i]);
		time=((time%M)*(time1%M)/gcd(time%M,time1%M))%M;
	}
	return (int)(time%M);
}

